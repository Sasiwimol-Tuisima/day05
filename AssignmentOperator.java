package day05_operators;

public class AssignmentOperator {

	public static void main(String[] args) {
		
		int a = 9;
		
		a += 5;    // a = a +5;
		System.out.println(a);  //14  ==> a = 9 +5;
		
		a += 5;    // a = a +5;
		System.out.println(a);  //19   ==> a = 14 +5
		
		a -= 10;   // a = a -10
		System.out.println(a);  // 9  ==> a = 19 -10
		
		a /= 3;   // a = a / 3
		System.out.println(a);  // 3  ==> a = 10 /3
		
		a *= 2;  // a = a *2
		System.out.println(a);  // 6 ==> a = 3 *2
		
		a *= 2;  // a = a *2
		System.out.println(a);  // 12 ==> a = 6 *2
		
		
		a=0;
		System.out.println(a);
		a=5;
		System.out.println(a);
		a=10;
		System.out.println(a);
		a=15;
		System.out.println(a);
		a=20;
		System.out.println(a);
		a=25;
		System.out.println(a);
		a=30;
		System.out.println(a);
		
		
		
		
		
		
		

	}

}
